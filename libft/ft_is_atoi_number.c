#include "libft.h"

bool	is_atoi_number(char *str)
{
	if (!*str)
	{
		return (0);
	}
	if (*str == '+' || *str == '-')
	{
		++str;
	}
	if (!(*str >= '0' && *str <= '9'))
	{
		return (0);
	}
	return (1);
}
