#include "libft.h"

void	ft_striter(char *s, void (*f)(char*))
{
	size_t i;
	size_t len;

	i = 0;
	if (!f)
		return ;
	len = ft_strlen(s);
	while (i < len)
	{
		f(&s[i]);
		++i;
	}
}
