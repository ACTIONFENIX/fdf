#include "libft.h"

int is_atox_digit(char c)
{
	if (c >= '0' && c <= '9')
	{
		return (1);
	}
	else if (c >= 'a' && c <= 'f')
	{
		return (1);
	}
	else if (c >= 'A' && c <= 'F')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
